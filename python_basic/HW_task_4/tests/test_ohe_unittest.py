    def test_empty_input(self):
        true = fit_transform([])
        pred = []
        self.assertEqual(true, pred)

    def test_single_word(self):
        true = fit_transform("EMINEM")
        pred = [("EMINEM", [1])]
        self.assertEqual(true, pred)

    def test_multiple_words_as_list(self):
        self.assertListEqual(fit_transform(self.INPUT), self.TRUE_OUTPUT)

    def test_wrong_input(self):
        with self.assertRaises(TypeError, msg="expected at least 1 arguments, got 0"):
            fit_transform()
